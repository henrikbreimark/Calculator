function calculate (operator, numberA, numberB) {
    return operator(numberA, numberB);
}

function add (numberA, numberB) {
    return numberA+numberB;
}

function add (numberA, numberB) {
    return numberA-numberB;
}

function multi (numberA, numberB) {
    return numberA*numberB;
}

function div (numberA, numberB) {
    return numberA/numberB;
}

